bool detect_key_down(void) {
  // store the previous cycle's cache
  for (uint8_t i = 0; i < KEYS_SIZE; ++i) {
    pressed_keys_prev[i].first = pressed_keys[i].first;
    pressed_keys_prev[i].second = pressed_keys[i].second;
  }

  // fill cache with currently pressed keys
  pressed_keys_index = 0;
  for (uint8_t x = 0; x < MATRIX_ROWS; x++) {
    for (uint8_t y = 0; y < MATRIX_COLS; y++) {
      // is this key is currently down?
      if (((matrix_get_row(x) & (1 << y)) > 0)) {
        pressed_keys[pressed_keys_index].first =
            x +
            1; // adding 1 to the row/col so that we can use 0 as a null-check
        pressed_keys[pressed_keys_index].second = y + 1;
      } else {
        pressed_keys[pressed_keys_index].first = 0;
        pressed_keys[pressed_keys_index].second = 0;
      }
      pressed_keys_index++;
    }
  }

  // check for a new key down compared to last cycle
  for (uint8_t i = 0; i < KEYS_SIZE; ++i) {
    if (pressed_keys[i].first && pressed_keys[i].second &&
        !pressed_keys_prev[i].first && !pressed_keys_prev[i].second) {
      return true;
    }
  }
  return false;
}

void eval_anim_state(void) {
  key_down = detect_key_down();

  switch (anim_state) {
  case Idle:
    if (key_down) // Idle to Tap
    {
      anim_state = Tap;
    }
    break;

  case Prep:
    if (key_down) // Prep to Tap
    {
      anim_state = Tap;
    } else if (timer_elapsed32(idle_timeout_timer) >=
               IDLE_TIMEOUT) // Prep to Idle
    {
      anim_state = Idle;
      current_idle_frame = 0;
    }
    break;

  case Tap:
    if (!key_down) // Tap to Prep
    {
      anim_state = Prep;
      idle_timeout_timer = timer_read32();
    }
    break;

  default:
    break;
  }
}

static void draw_bongo(bool minimal) {
  eval_anim_state();

  oled_set_cursor(0, 0);

  switch (anim_state) {
  case Idle:
    if (minimal)
      oled_write_raw_P(
          idle_minimal[abs((IDLE_FRAMES - 1) - current_idle_frame)], ANIM_SIZE);
    else
      oled_write_raw_P(idle[abs((IDLE_FRAMES - 1) - current_idle_frame)],
                       ANIM_SIZE);
    if (timer_elapsed32(anim_timer) > ANIM_FRAME_DURATION) {
      current_idle_frame = (current_idle_frame + 1) % IDLE_FRAMES;
      anim_timer = timer_read32();
    }
    break;

  case Prep:
    if (minimal)
      oled_write_raw_P(prep_minimal[0], ANIM_SIZE);
    else
      oled_write_raw_P(prep[0], ANIM_SIZE);
    break;

  case Tap:
    if (minimal)
      oled_write_raw_P(tap_minimal[abs((TAP_FRAMES - 1) - current_tap_frame)],
                       ANIM_SIZE);
    else
      oled_write_raw_P(tap[abs((TAP_FRAMES - 1) - current_tap_frame)],
                       ANIM_SIZE);
    current_tap_frame = (current_tap_frame + 1) % TAP_FRAMES;
    break;

  default:
    break;
  }
}

bool oled_task_user(void) {
  if (!oled_enabled) {
    oled_clear();
    oled_off();
  } else {
    oled_on();
    if (oled_msg_idx < 2) {
      if (is_keyboard_master()) {
        oled_clear();
        draw_bongo(true);
        oled_set_cursor(0, 0);
        oled_write("hi boo!", false);
      }
    } else if (oled_msg_idx < 3) {
      if (is_keyboard_master()) {
        oled_clear();
        draw_bongo(true);
        oled_set_cursor(0, 0);
        oled_write("hi xty!", false);
      }
    } else {
      if (is_keyboard_master()) {
        oled_clear();
        oled_write("don't complain so much", false);
      }
    }
  }

  return false;
}

