bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
  case TOGGLE_OLED:
    if (record->event.pressed) {
      oled_enabled = !oled_enabled;
    };
    return true;
  default:
    return true;
  }
}

